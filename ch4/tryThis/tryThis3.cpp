/*
    This program prints ASCII characters and their corressponding
    integer values against them.
*/

#include <iostream> 
#include <cmath> 

int main()
{
    int i = 0;
    while (i < 58) {
        std::cout << char('A'+i) << '\t' << 'A'+i << '\n';
        ++i; 
        }
        
    return 0;
}
